create table if not exists public.club_invites (
  id bigint generated by default as identity primary key,
  club_id bigint not null
    references public.clubs(id) on delete cascade,
  created_by_user_id uuid
    references auth.users(id) on delete set null,
  created_by_membership_id bigint
    references public.memberships(id) on delete set null,
  target_user_id uuid not null
    references auth.users(id) on delete cascade,
  target_player_profile_id bigint
    references public.player_profiles(id) on delete set null,
  target_account_email text,
  target_nome text not null,
  target_cognome text not null,
  target_id_console text,
  target_primary_role text,
  status text not null default 'pending',
  resolved_at timestamptz,
  resolved_by_user_id uuid
    references auth.users(id) on delete set null,
  resolved_by_membership_id bigint
    references public.memberships(id) on delete set null,
  accepted_membership_id bigint
    references public.memberships(id) on delete set null,
  accepted_player_id bigint
    references public.player_profiles(id) on delete set null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint club_invites_status_check
    check (status in ('pending', 'accepted', 'declined', 'revoked', 'expired')),
  constraint club_invites_target_nome_not_blank
    check (char_length(btrim(target_nome)) > 0),
  constraint club_invites_target_cognome_not_blank
    check (char_length(btrim(target_cognome)) > 0),
  constraint club_invites_target_account_email_check
    check (
      target_account_email is null
      or (
        trim(target_account_email) <> ''
        and position('@' in target_account_email) > 1
      )
    ),
  constraint club_invites_self_invite_check
    check (created_by_user_id is null or created_by_user_id <> target_user_id)
);

drop trigger if exists club_invites_touch_updated_at on public.club_invites;

