create or replace function public.clubline_create_club(
  p_actor_user_id uuid,
  p_actor_email text,
  p_name text,
  p_owner_nome text,
  p_owner_cognome text,
  p_owner_id_console text,
  p_owner_shirt_number integer default null,
  p_owner_primary_role text default null,
  p_primary_color text default null,
  p_accent_color text default null,
  p_surface_color text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_name text := public.clubline_normalize_text(p_name);
  v_owner_nome text := public.clubline_normalize_text(p_owner_nome);
  v_owner_cognome text := public.clubline_normalize_text(p_owner_cognome);
  v_owner_console text := public.clubline_normalize_text(p_owner_id_console);
  v_slug text;
  v_club public.clubs%rowtype;
  v_membership public.memberships%rowtype;
  v_expired_invite public.club_invites%rowtype;
  v_expired_invite_ids bigint[] := '{}'::bigint[];
  v_player_id bigint;
  v_now timestamptz := timezone('utc', now());
begin
  if v_name is null then
    raise exception using errcode = '22023', message = 'Inserisci un nome club valido';
  end if;
  if v_owner_nome is null or v_owner_cognome is null or v_owner_console is null then
    raise exception using errcode = '22023', message = 'Inserisci nome, cognome e ID console del capitano';
  end if;

  perform public.clubline_lock_user_membership_flow(p_actor_user_id);

  perform 1
  from public.memberships
  where auth_user_id = p_actor_user_id
    and status = 'active'
  for update;

  if found then
    raise exception using errcode = 'P0001', message = 'Appartieni gia a un club attivo';
  end if;

  perform 1
  from public.join_requests
  where requester_user_id = p_actor_user_id
    and status = 'pending'
  for update;

  if found then
    raise exception using errcode = 'P0001', message = 'Hai gia una richiesta di ingresso pendente';
  end if;

  v_slug := public.clubline_next_club_slug(v_name);

  insert into public.clubs (
    name,
    normalized_name,
    slug,
    primary_color,
    accent_color,
    surface_color,
    created_by_user_id
  )
  values (
    v_name,
    lower(v_name),
    v_slug,
    coalesce(public.clubline_normalize_text(p_primary_color), '#1F2937'),
    coalesce(public.clubline_normalize_text(p_accent_color), '#0F766E'),
    coalesce(public.clubline_normalize_text(p_surface_color), '#0F172A'),
    p_actor_user_id
  )
  returning * into v_club;

  insert into public.club_settings (
    club_id,
    additional_links
  )
  values (
    v_club.id,
    '[]'::jsonb
  )
  on conflict (club_id) do nothing;

  insert into public.club_permission_settings (
    club_id,
    vice_manage_players,
    vice_manage_lineups,
    vice_manage_streams,
    vice_manage_attendance,
    vice_manage_team_info
  )
  values (
    v_club.id,
    false,
    false,
    false,
    false,
    false
  )
  on conflict (club_id) do nothing;

  begin
    insert into public.memberships (
      club_id,
      auth_user_id,
      role,
      status
    )
    values (
      v_club.id,
      p_actor_user_id,
      'captain',
      'active'
    )
    returning * into v_membership;
  exception
    when unique_violation then
      raise exception using errcode = 'P0001', message = 'Appartieni gia a un club attivo';
  end;

  v_player_id := public.clubline_attach_player_profile(
    v_membership.id,
    p_actor_user_id,
    p_actor_email,
    v_owner_nome,
    v_owner_cognome,
    v_owner_console,
    p_owner_shirt_number,
    p_owner_primary_role,
    'captain'
  );

  for v_expired_invite in
    update public.club_invites
    set status = 'expired',
        resolved_at = v_now,
        resolved_by_user_id = p_actor_user_id,
        resolved_by_membership_id = v_membership.id
    where target_user_id = p_actor_user_id
      and status = 'pending'
    returning *
  loop
    v_expired_invite_ids := array_append(v_expired_invite_ids, v_expired_invite.id);

    perform public.clubline_log_event(
      v_expired_invite.club_id,
      null,
      p_actor_user_id,
      p_actor_user_id,
      'invite_expired',
      jsonb_build_object(
        'invite_id', v_expired_invite.id,
        'reason', 'club_created',
        'club_id', v_club.id,
        'membership_id', v_membership.id
      )
    );
  end loop;

  if cardinality(v_expired_invite_ids) > 0 then
    update public.app_notifications
    set read_at = coalesce(read_at, v_now)
    where recipient_user_id = p_actor_user_id
      and notification_type = 'club_invite_received'
      and related_invite_id = any(v_expired_invite_ids);
  end if;

  perform public.clubline_log_event(
    v_club.id,
    v_membership.id,
    p_actor_user_id,
    p_actor_user_id,
    'club_created',
    jsonb_build_object(
      'club_id', v_club.id,
      'membership_id', v_membership.id,
      'player_id', v_player_id
    )
  );

  return jsonb_build_object(
    'club_id', v_club.id,
    'membership_id', v_membership.id,
    'player_id', v_player_id
  );
end;
$$;

