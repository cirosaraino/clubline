  );
end;
$$;

create or replace function public.clubline_create_notification(
  p_recipient_user_id uuid,
  p_club_id bigint,
  p_notification_type text,
  p_title text,
  p_body text default null,
  p_metadata jsonb default '{}'::jsonb,
  p_related_invite_id bigint default null,
  p_dedupe_key text default null
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_notification_id bigint;
  v_notification_type text := public.clubline_normalize_text(p_notification_type);
begin
  if p_recipient_user_id is null then
    raise exception using
      errcode = '22023',
      message = 'Destinatario notifica non valido',
      detail = 'invalid_notification_recipient';
  end if;

  if v_notification_type is null then
    raise exception using
      errcode = '22023',
      message = 'Tipo notifica obbligatorio',
      detail = 'notification_type_required';
  end if;

  if public.clubline_normalize_text(p_title) is null then
    raise exception using
      errcode = '22023',
      message = 'Titolo notifica obbligatorio',
      detail = 'notification_title_required';
  end if;

  if coalesce(jsonb_typeof(coalesce(p_metadata, '{}'::jsonb)), 'null') <> 'object' then
    raise exception using
      errcode = '22023',
      message = 'Metadata notifica non validi',
      detail = 'invalid_notification_metadata';
  end if;

  insert into public.app_notifications (
    recipient_user_id,
    club_id,
    notification_type,
    title,
    body,
    metadata,
    related_invite_id,
    dedupe_key
  )
  values (
    p_recipient_user_id,
    p_club_id,
    v_notification_type,
    btrim(p_title),
    public.clubline_normalize_text(p_body),
    coalesce(p_metadata, '{}'::jsonb),
    p_related_invite_id,
    public.clubline_normalize_text(p_dedupe_key)
  )
  on conflict (recipient_user_id, dedupe_key)
  where dedupe_key is not null
  do update
    set club_id = excluded.club_id,
        notification_type = excluded.notification_type,
        title = excluded.title,
        body = excluded.body,
        metadata = excluded.metadata,
        related_invite_id = coalesce(excluded.related_invite_id, public.app_notifications.related_invite_id)
  returning id into v_notification_id;

  return v_notification_id;
end;
$$;

create or replace function public.clubline_create_club_invite(
  p_actor_user_id uuid,
  p_target_user_id uuid
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor_membership public.memberships%rowtype;
  v_permissions public.club_permission_settings%rowtype;
  v_target_player public.player_profiles%rowtype;
  v_target_active_membership public.memberships%rowtype;
  v_pending_join_request public.join_requests%rowtype;
  v_existing_invite public.club_invites%rowtype;
  v_club public.clubs%rowtype;
  v_invite public.club_invites%rowtype;
  v_target_email text;
  v_target_nome text;
  v_target_cognome text;
  v_notification_id bigint;
begin
  if p_actor_user_id is null or p_target_user_id is null then
    raise exception using
      errcode = '22023',
      message = 'Utenti invito non validi',
      detail = 'invalid_invite_users';
  end if;

  if p_actor_user_id = p_target_user_id then
    raise exception using
      errcode = 'P0001',
      message = 'Non puoi invitare te stesso',
      detail = 'self_invite_forbidden';
  end if;

  select *
    into v_actor_membership
  from public.memberships
  where auth_user_id = p_actor_user_id
    and status = 'active'
  for update;

  if not found then
    raise exception using
      errcode = '42501',
      message = 'Devi appartenere a un club per inviare un invito',
      detail = 'invite_management_forbidden';
  end if;

  select *
    into v_permissions
  from public.club_permission_settings
  where club_id = v_actor_membership.club_id;

  if not (
    v_actor_membership.role = 'captain'
    or (
      v_actor_membership.role = 'vice_captain'
      and coalesce(v_permissions.vice_manage_invites, false)
    )
  ) then
    raise exception using
      errcode = '42501',
      message = 'Non hai i permessi per gestire gli inviti del club',
      detail = 'invite_management_forbidden';
  end if;

  perform public.clubline_lock_user_membership_flow(p_target_user_id);

  select email
    into v_target_email
  from auth.users
  where id = p_target_user_id;

  if not found then
    raise exception using
      errcode = '22023',
      message = 'Utente di destinazione non trovato',
      detail = 'target_user_not_found';
  end if;

  select *
    into v_target_active_membership
  from public.memberships
  where auth_user_id = p_target_user_id
    and status = 'active'
  for update;

  if found then
    raise exception using
      errcode = 'P0001',
      message = 'L utente appartiene gia a un club attivo',
      detail = 'target_user_has_active_membership';
  end if;

  select *
    into v_pending_join_request
  from public.join_requests
  where requester_user_id = p_target_user_id
    and status = 'pending'
  order by created_at asc, id asc
  limit 1
  for update;

  if found then
    if v_pending_join_request.club_id = v_actor_membership.club_id then
      raise exception using
        errcode = 'P0001',
        message = 'Esiste gia una richiesta di ingresso pendente per questo club',
        detail = 'pending_join_request_same_club';
    end if;

    raise exception using
      errcode = 'P0001',
      message = 'L utente ha una richiesta di ingresso pendente verso un altro club',
      detail = 'pending_join_request_other_club';
  end if;

  select *
    into v_existing_invite
  from public.club_invites
  where club_id = v_actor_membership.club_id
    and target_user_id = p_target_user_id
    and status = 'pending'
  order by created_at desc, id desc
  limit 1
  for update;

  if found then
    raise exception using
      errcode = 'P0001',
      message = 'Esiste gia un invito pendente per questo utente in questo club',
      detail = 'pending_club_invite_exists';
  end if;

  select *
    into v_target_player
  from public.player_profiles
  where auth_user_id = p_target_user_id
    and archived_at is null
  order by
    case
      when club_id is null and membership_id is null then 0
      else 1
    end asc,
    created_at desc nulls last,
    id desc
  limit 1
  for update;

  v_target_nome := coalesce(
    public.clubline_normalize_text(v_target_player.nome),
    public.clubline_normalize_text(
      initcap(
        split_part(
          regexp_replace(split_part(coalesce(v_target_email, ''), '@', 1), '[^a-zA-Z0-9]+', ' ', 'g'),
          ' ',
          1
        )
      )
    ),
    'Nuovo'
  );
  v_target_cognome := coalesce(
    public.clubline_normalize_text(v_target_player.cognome),
    'Membro'
  );

  select *
    into v_club
  from public.clubs
  where id = v_actor_membership.club_id;

  begin
    insert into public.club_invites (
      club_id,
      created_by_user_id,
      created_by_membership_id,
      target_user_id,
      target_player_profile_id,
      target_account_email,
      target_nome,
      target_cognome,
      target_id_console,
      target_primary_role,
      status
    )
    values (
      v_actor_membership.club_id,
      p_actor_user_id,
      v_actor_membership.id,
      p_target_user_id,
      v_target_player.id,
      public.clubline_normalize_email(coalesce(v_target_player.account_email, v_target_email)),
      v_target_nome,
      v_target_cognome,
      public.clubline_normalize_text(v_target_player.id_console),
      public.clubline_normalize_text(v_target_player.primary_role),
      'pending'
    )
    returning * into v_invite;
  exception
    when unique_violation then
      raise exception using
        errcode = 'P0001',
        message = 'Esiste gia un invito pendente per questo utente in questo club',
        detail = 'pending_club_invite_exists';
  end;

  v_notification_id := public.clubline_create_notification(
    p_target_user_id,
    v_actor_membership.club_id,
    'club_invite_received',
    'Hai ricevuto un invito al club ' || coalesce(v_club.name, 'Clubline'),
    'Apri l app per accettare o rifiutare l invito.',
    jsonb_build_object(
      'redirect', jsonb_build_object(
        'screen', 'notifications',
        'entity', 'club_invite',
        'invite_id', v_invite.id
      ),
      'invite_id', v_invite.id,
      'club_id', v_actor_membership.club_id
    ),
    v_invite.id,
    'club_invite_received:' || v_invite.id::text
  );

  perform public.clubline_log_event(
    v_actor_membership.club_id,
    v_actor_membership.id,
    p_actor_user_id,
    p_target_user_id,
    'invite_created',
    jsonb_build_object(
      'invite_id', v_invite.id,
      'notification_id', v_notification_id
    )
  );

  return jsonb_build_object(
    'invite_id', v_invite.id,
    'club_id', v_invite.club_id,
    'target_user_id', v_invite.target_user_id,
    'notification_id', v_notification_id
  );
end;
$$;

create or replace function public.clubline_revoke_club_invite(
  p_actor_user_id uuid,
  p_invite_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor_membership public.memberships%rowtype;
  v_permissions public.club_permission_settings%rowtype;
  v_invite public.club_invites%rowtype;
  v_club public.clubs%rowtype;
  v_now timestamptz := timezone('utc', now());
  v_notification_id bigint;
begin
  select *
    into v_actor_membership
  from public.memberships
  where auth_user_id = p_actor_user_id
    and status = 'active'
  for update;

  if not found then
    raise exception using
      errcode = '42501',
      message = 'Devi appartenere a un club per revocare un invito',
      detail = 'invite_management_forbidden';
  end if;

  select *
    into v_permissions
  from public.club_permission_settings
  where club_id = v_actor_membership.club_id;

  if not (
    v_actor_membership.role = 'captain'
    or (
      v_actor_membership.role = 'vice_captain'
      and coalesce(v_permissions.vice_manage_invites, false)
    )
  ) then
    raise exception using
      errcode = '42501',
      message = 'Non hai i permessi per gestire gli inviti del club',
      detail = 'invite_management_forbidden';
  end if;

  select *
    into v_invite
  from public.club_invites
  where id = p_invite_id
  for update;

  if not found then
    raise exception using
      errcode = '22023',
      message = 'Invito non trovato',
      detail = 'invite_not_found';
  end if;

  if v_invite.club_id <> v_actor_membership.club_id then
    raise exception using
      errcode = '42501',
      message = 'Non puoi revocare inviti di un altro club',
      detail = 'invite_management_forbidden';
  end if;

  if v_invite.status <> 'pending' then
    raise exception using
      errcode = 'P0001',
      message = 'Questo invito e gia stato gestito',
      detail = 'invite_not_pending';
  end if;

  update public.club_invites
  set status = 'revoked',
      resolved_at = v_now,
      resolved_by_user_id = p_actor_user_id,
      resolved_by_membership_id = v_actor_membership.id
  where id = v_invite.id
  returning * into v_invite;

  update public.app_notifications
  set read_at = coalesce(read_at, v_now)
  where related_invite_id = v_invite.id
    and notification_type = 'club_invite_received'
    and recipient_user_id = v_invite.target_user_id;

  select *
    into v_club
  from public.clubs
  where id = v_invite.club_id;

  v_notification_id := public.clubline_create_notification(
    v_invite.target_user_id,
    v_invite.club_id,
    'club_invite_revoked',
    'Invito revocato per il club ' || coalesce(v_club.name, 'Clubline'),
    'L invito al club e stato revocato.',
    jsonb_build_object(
      'redirect', jsonb_build_object(
        'screen', 'notifications',
        'entity', 'club_invite',
        'invite_id', v_invite.id
      ),
      'invite_id', v_invite.id,
      'club_id', v_invite.club_id
    ),
    v_invite.id,
    'club_invite_revoked:' || v_invite.id::text
  );

  perform public.clubline_log_event(
    v_invite.club_id,
    v_actor_membership.id,
    p_actor_user_id,
    v_invite.target_user_id,
    'invite_revoked',
    jsonb_build_object(
      'invite_id', v_invite.id,
      'notification_id', v_notification_id
    )
  );

  return jsonb_build_object(
    'invite_id', v_invite.id,
    'status', v_invite.status
  );
end;
$$;

create or replace function public.clubline_accept_club_invite(
  p_actor_user_id uuid,
  p_invite_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invite public.club_invites%rowtype;
  v_existing_membership public.memberships%rowtype;
  v_membership public.memberships%rowtype;
  v_expired_invite public.club_invites%rowtype;
  v_expired_invite_ids bigint[] := '{}'::bigint[];
  v_club public.clubs%rowtype;
  v_player_id bigint;
  v_now timestamptz := timezone('utc', now());
  v_notification_id bigint;
begin
  select *
    into v_invite
  from public.club_invites
  where id = p_invite_id;

  if not found then
    raise exception using
      errcode = '22023',
      message = 'Invito non trovato',
      detail = 'invite_not_found';
  end if;

  if v_invite.target_user_id <> p_actor_user_id then
    raise exception using
      errcode = '42501',
      message = 'Questo invito non appartiene all utente corrente',
      detail = 'invite_not_owned';
  end if;

  perform public.clubline_lock_user_membership_flow(p_actor_user_id);

  select *
    into v_invite
  from public.club_invites
  where id = p_invite_id
  for update;

  if not found then
    raise exception using
      errcode = '22023',
      message = 'Invito non trovato',
      detail = 'invite_not_found';
  end if;

  if v_invite.target_user_id <> p_actor_user_id then
    raise exception using
      errcode = '42501',
      message = 'Questo invito non appartiene all utente corrente',
      detail = 'invite_not_owned';
  end if;

  if v_invite.status <> 'pending' then
    raise exception using
      errcode = 'P0001',
      message = 'Questo invito e gia stato gestito',
      detail = 'invite_not_pending';
  end if;

  select *
    into v_existing_membership
  from public.memberships
  where auth_user_id = p_actor_user_id
    and status = 'active'
  for update;

  if found then
    raise exception using
      errcode = 'P0001',
      message = 'Appartieni gia a un club attivo',
      detail = 'target_user_has_active_membership';
  end if;

  perform 1
  from public.join_requests
  where requester_user_id = p_actor_user_id
    and status = 'pending'
  for update;

  begin
    insert into public.memberships (
      club_id,
      auth_user_id,
      role,
      status
    )
    values (
      v_invite.club_id,
      p_actor_user_id,
      'player',
      'active'
    )
    returning * into v_membership;
  exception
    when unique_violation then
      raise exception using
        errcode = 'P0001',
        message = 'Appartieni gia a un club attivo',
        detail = 'target_user_has_active_membership';
  end;

  v_player_id := public.clubline_attach_player_profile(
    v_membership.id,
    p_actor_user_id,
    v_invite.target_account_email,
    v_invite.target_nome,
    v_invite.target_cognome,
    v_invite.target_id_console,
    null,
    v_invite.target_primary_role,
    'player'
  );

  update public.club_invites
  set status = 'accepted',
      resolved_at = v_now,
      resolved_by_user_id = p_actor_user_id,
      resolved_by_membership_id = v_membership.id,
      accepted_membership_id = v_membership.id,
      accepted_player_id = v_player_id
  where id = v_invite.id
  returning * into v_invite;

  update public.join_requests
  set status = 'expired',
      decided_at = v_now,
      decided_by_membership_id = null
  where requester_user_id = p_actor_user_id
    and status = 'pending';

  for v_expired_invite in
    update public.club_invites
    set status = 'expired',
        resolved_at = v_now,
        resolved_by_user_id = p_actor_user_id,
        resolved_by_membership_id = v_membership.id
    where target_user_id = p_actor_user_id
      and status = 'pending'
      and id <> v_invite.id
    returning *
  loop
    v_expired_invite_ids := array_append(v_expired_invite_ids, v_expired_invite.id);

    perform public.clubline_log_event(
      v_expired_invite.club_id,
      null,
      p_actor_user_id,
      p_actor_user_id,
      'invite_expired',
      jsonb_build_object(
        'invite_id', v_expired_invite.id,
        'reason', 'invite_accepted',
        'accepted_invite_id', v_invite.id,
