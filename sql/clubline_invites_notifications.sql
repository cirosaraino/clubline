create or replace function public.clubline_current_user_can_manage_invites_for_club(
  target_club_id bigint
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.memberships as membership
    left join public.club_permission_settings as permissions
      on permissions.club_id = membership.club_id
    where membership.auth_user_id = auth.uid()
      and membership.club_id = target_club_id
      and membership.status = 'active'
      and (
        membership.role = 'captain'
        or (
          membership.role = 'vice_captain'
          and coalesce(permissions.vice_manage_invites, false)
        )
      )
  )
$$;

create or replace function public.clubline_lock_user_membership_flow(
  target_user_id uuid
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
