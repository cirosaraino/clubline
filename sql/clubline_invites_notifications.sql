      jsonb_build_object(
        'redirect', jsonb_build_object(
          'screen', 'club_management',
          'entity', 'club_invite',
          'invite_id', v_invite.id
        ),
        'invite_id', v_invite.id,
        'club_id', v_invite.club_id,
