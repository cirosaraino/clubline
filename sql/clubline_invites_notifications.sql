        'player_id', v_player_id
      ),
      v_invite.id,
      'club_invite_accepted:' || v_invite.id::text
    );
  else
    v_notification_id := null;
  end if;

  perform public.clubline_log_event(
    v_invite.club_id,
    v_membership.id,
    p_actor_user_id,
    p_actor_user_id,
    'invite_accepted',
