  title text not null,
  body text,
  metadata jsonb not null default '{}'::jsonb,
  related_invite_id bigint
    references public.club_invites(id) on delete set null,
  read_at timestamptz,
  dedupe_key text,
  created_at timestamptz not null default timezone('utc', now()),
