alter table if exists public.player_profiles
  add column if not exists club_id bigint references public.clubs(id) on delete cascade;

alter table if exists public.player_profiles
  add column if not exists membership_id bigint references public.memberships(id) on delete set null;

alter table if exists public.player_profiles
  add column if not exists archived_at timestamptz;

alter table if exists public.stream_links
  add column if not exists club_id bigint references public.clubs(id) on delete cascade;

alter table if exists public.lineups
  add column if not exists club_id bigint references public.clubs(id) on delete cascade;

alter table if exists public.attendance_weeks
  add column if not exists club_id bigint references public.clubs(id) on delete cascade;

alter table if exists public.attendance_entries
  add column if not exists club_id bigint references public.clubs(id) on delete cascade;

do $$
declare
  legacy_club_id bigint;
  should_migrate_legacy boolean;
begin
  select
    exists (select 1 from public.player_profiles)
    or exists (select 1 from public.stream_links)
    or exists (select 1 from public.lineups)
    or exists (select 1 from public.attendance_weeks)
    or exists (
      select 1
      from public.team_settings
      where id = 1
        and (
          lower(coalesce(nullif(btrim(team_name), ''), '')) not in ('', 'ultras mentality', 'clubline')
          or crest_url is not null
          or website_url is not null
          or youtube_url is not null
          or discord_url is not null
          or facebook_url is not null
          or instagram_url is not null
          or twitch_url is not null
          or tiktok_url is not null
          or jsonb_array_length(additional_links) > 0
        )
    )
    or exists (
      select 1
      from public.team_permission_settings
      where id = 1
        and (
          vice_manage_players is distinct from true
          or vice_manage_lineups is distinct from true
          or vice_manage_streams is distinct from true
          or vice_manage_attendance is distinct from true
          or vice_manage_team_info is distinct from false
        )
    )
  into should_migrate_legacy;

  if should_migrate_legacy then
    insert into public.clubs (
    name,
    normalized_name,
    slug,
    logo_url,
    primary_color,
    accent_color,
    surface_color
  )
  values (
    coalesce(
      (select nullif(btrim(team_name), '') from public.team_settings where id = 1),
      'Clubline Legacy'
    ),
    lower(
      coalesce(
        (select nullif(btrim(team_name), '') from public.team_settings where id = 1),
        'clubline legacy'
      )
    ),
    'legacy-club',
    (select crest_url from public.team_settings where id = 1),
    '#1F2937',
    '#0F766E',
    '#0F172A'
  )
    on conflict (slug) do update
      set updated_at = timezone('utc', now())
    returning id into legacy_club_id;

    insert into public.club_settings (
    club_id,
    website_url,
    youtube_url,
    discord_url,
    facebook_url,
    instagram_url,
    twitch_url,
    tiktok_url,
    additional_links
  )
  select
    legacy_club_id,
    website_url,
    youtube_url,
    discord_url,
    facebook_url,
    instagram_url,
    twitch_url,
    tiktok_url,
    additional_links
    from public.team_settings
    where id = 1
    on conflict (club_id) do update
      set website_url = excluded.website_url,
          youtube_url = excluded.youtube_url,
          discord_url = excluded.discord_url,
          facebook_url = excluded.facebook_url,
          instagram_url = excluded.instagram_url,
          twitch_url = excluded.twitch_url,
          tiktok_url = excluded.tiktok_url,
          additional_links = excluded.additional_links,
          updated_at = timezone('utc', now());

    insert into public.club_permission_settings (
    club_id,
    vice_manage_players,
    vice_manage_lineups,
    vice_manage_streams,
    vice_manage_attendance,
    vice_manage_team_info
  )
  select
    legacy_club_id,
    vice_manage_players,
    vice_manage_lineups,
    vice_manage_streams,
    vice_manage_attendance,
    vice_manage_team_info
    from public.team_permission_settings
    where id = 1
    on conflict (club_id) do update
      set vice_manage_players = excluded.vice_manage_players,
          vice_manage_lineups = excluded.vice_manage_lineups,
          vice_manage_streams = excluded.vice_manage_streams,
          vice_manage_attendance = excluded.vice_manage_attendance,
          vice_manage_team_info = excluded.vice_manage_team_info,
          updated_at = timezone('utc', now());

    update public.player_profiles
    set club_id = legacy_club_id
    where club_id is null;

    update public.stream_links
    set club_id = legacy_club_id
    where club_id is null;

    update public.lineups
    set club_id = legacy_club_id
    where club_id is null;

    update public.attendance_weeks
    set club_id = legacy_club_id
    where club_id is null;

    update public.attendance_entries
    set club_id = legacy_club_id
    where club_id is null;

    insert into public.memberships (
    club_id,
    auth_user_id,
    role,
    status
  )
  select
    legacy_club_id,
    player.auth_user_id,
    player.team_role,
    'active'
    from public.player_profiles as player
    where player.auth_user_id is not null
      and player.archived_at is null
      and not exists (
        select 1
        from public.memberships as membership
        where membership.auth_user_id = player.auth_user_id
          and membership.status = 'active'
      );

    update public.player_profiles as player
    set membership_id = membership.id
    from public.memberships as membership
    where player.auth_user_id is not null
      and membership.auth_user_id = player.auth_user_id
      and membership.status = 'active'
      and player.membership_id is null;
  end if;
end;
$$;

