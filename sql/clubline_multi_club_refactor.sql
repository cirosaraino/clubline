begin;

create table if not exists public.clubs (
  id bigint generated by default as identity primary key,
  name text not null,
  normalized_name text not null,
  slug text not null,
  logo_url text,
  logo_storage_path text,
  primary_color text,
  accent_color text,
  surface_color text,
  created_by_user_id uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint clubs_name_not_blank check (char_length(btrim(name)) > 0),
  constraint clubs_normalized_name_not_blank check (char_length(btrim(normalized_name)) > 0),
  constraint clubs_slug_not_blank check (char_length(btrim(slug)) > 0)
);

create unique index if not exists clubs_normalized_name_unique
  on public.clubs (normalized_name);

create unique index if not exists clubs_slug_unique
  on public.clubs (slug);

create table if not exists public.club_settings (
  club_id bigint primary key
    references public.clubs(id) on delete cascade,
  website_url text,
  youtube_url text,
  discord_url text,
  facebook_url text,
  instagram_url text,
  twitch_url text,
  tiktok_url text,
  additional_links jsonb not null default '[]'::jsonb,
  updated_at timestamptz not null default timezone('utc', now()),
  constraint club_settings_additional_links_is_array_check
    check (jsonb_typeof(additional_links) = 'array')
);

create table if not exists public.club_permission_settings (
  club_id bigint primary key
    references public.clubs(id) on delete cascade,
  vice_manage_players boolean not null default false,
  vice_manage_lineups boolean not null default false,
  vice_manage_streams boolean not null default false,
  vice_manage_attendance boolean not null default false,
  vice_manage_team_info boolean not null default false,
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.memberships (
  id bigint generated by default as identity primary key,
  club_id bigint not null
    references public.clubs(id) on delete cascade,
  auth_user_id uuid not null
    references auth.users(id) on delete cascade,
  role text not null default 'player',
  status text not null default 'active',
  left_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint memberships_role_check
    check (role in ('player', 'vice_captain', 'captain')),
  constraint memberships_status_check
    check (status in ('active', 'left'))
);

create unique index if not exists memberships_user_active_unique
  on public.memberships (auth_user_id)
  where status = 'active';

create unique index if not exists memberships_club_captain_unique
  on public.memberships (club_id)
  where status = 'active' and role = 'captain';

create index if not exists memberships_club_status_idx
  on public.memberships (club_id, status);

create table if not exists public.join_requests (
  id bigint generated by default as identity primary key,
  club_id bigint not null
    references public.clubs(id) on delete cascade,
  requester_user_id uuid not null
    references auth.users(id) on delete cascade,
  requester_email text,
  requested_nome text not null,
  requested_cognome text not null,
  requested_shirt_number integer,
  requested_primary_role text,
  status text not null default 'pending',
  decided_by_membership_id bigint
    references public.memberships(id) on delete set null,
  decided_at timestamptz,
  cancelled_at timestamptz,
  expires_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint join_requests_status_check
    check (status in ('pending', 'approved', 'rejected', 'cancelled', 'expired'))
);

alter table public.join_requests
  drop constraint if exists join_requests_requested_nome_not_blank;

alter table public.join_requests
  add constraint join_requests_requested_nome_not_blank
  check (char_length(btrim(requested_nome)) > 0);

alter table public.join_requests
  drop constraint if exists join_requests_requested_cognome_not_blank;

alter table public.join_requests
  add constraint join_requests_requested_cognome_not_blank
  check (char_length(btrim(requested_cognome)) > 0);

create unique index if not exists join_requests_user_pending_unique
  on public.join_requests (requester_user_id)
  where status = 'pending';

create index if not exists join_requests_club_status_idx
  on public.join_requests (club_id, status, created_at);

create table if not exists public.leave_requests (
  id bigint generated by default as identity primary key,
  club_id bigint not null
    references public.clubs(id) on delete cascade,
  membership_id bigint not null
    references public.memberships(id) on delete cascade,
  requested_by_user_id uuid not null
    references auth.users(id) on delete cascade,
  status text not null default 'pending',
  decided_by_membership_id bigint
    references public.memberships(id) on delete set null,
  decided_at timestamptz,
  cancelled_at timestamptz,
  expires_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint leave_requests_status_check
    check (status in ('pending', 'approved', 'rejected', 'cancelled', 'expired'))
);

create unique index if not exists leave_requests_membership_pending_unique
  on public.leave_requests (membership_id)
  where status = 'pending';

create index if not exists leave_requests_club_status_idx
  on public.leave_requests (club_id, status, created_at);

create or replace function public.touch_clubline_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

drop trigger if exists clubs_touch_updated_at on public.clubs;

create trigger clubs_touch_updated_at
before update on public.clubs
for each row
execute function public.touch_clubline_updated_at();

drop trigger if exists club_settings_touch_updated_at on public.club_settings;

create trigger club_settings_touch_updated_at
before update on public.club_settings
for each row
execute function public.touch_clubline_updated_at();

drop trigger if exists club_permission_settings_touch_updated_at on public.club_permission_settings;

create trigger club_permission_settings_touch_updated_at
before update on public.club_permission_settings
for each row
execute function public.touch_clubline_updated_at();

drop trigger if exists memberships_touch_updated_at on public.memberships;

create trigger memberships_touch_updated_at
before update on public.memberships
for each row
execute function public.touch_clubline_updated_at();

drop trigger if exists join_requests_touch_updated_at on public.join_requests;

create trigger join_requests_touch_updated_at
before update on public.join_requests
for each row
execute function public.touch_clubline_updated_at();

drop trigger if exists leave_requests_touch_updated_at on public.leave_requests;

create trigger leave_requests_touch_updated_at
before update on public.leave_requests
for each row
execute function public.touch_clubline_updated_at();

