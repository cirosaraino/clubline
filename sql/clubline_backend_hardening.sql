create or replace function public.clubline_next_club_slug(base_name text)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  normalized_base text := public.clubline_slugify(base_name);
  candidate text := normalized_base;
  suffix integer := 2;
begin
  while exists (
    select 1
    from public.clubs
    where slug = candidate
  ) loop
    candidate := normalized_base || '-' || suffix::text;
    suffix := suffix + 1;
  end loop;

  return candidate;
end;
$$;

create or replace function public.clubline_attach_player_profile(
  p_membership_id bigint,
  p_auth_user_id uuid,
  p_account_email text,
  p_nome text,
  p_cognome text,
  p_id_console text,
  p_shirt_number integer,
  p_primary_role text,
  p_team_role text
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_membership public.memberships%rowtype;
  v_player public.player_profiles%rowtype;
  v_email text := public.clubline_normalize_email(p_account_email);
  v_console text := public.clubline_normalize_text(p_id_console);
  v_nome text := coalesce(public.clubline_normalize_text(p_nome), 'Clubline');
  v_cognome text := coalesce(public.clubline_normalize_text(p_cognome), 'Player');
  v_team_role text := coalesce(public.clubline_normalize_text(p_team_role), 'player');
begin
  select *
    into v_membership
  from public.memberships
  where id = p_membership_id
  for update;

  if not found or v_membership.status <> 'active' then
    raise exception using errcode = 'P0001', message = 'Membership attiva non trovata';
  end if;

  select *
    into v_player
  from public.player_profiles
  where membership_id = p_membership_id
    and archived_at is null
  for update;

  if not found and p_auth_user_id is not null then
    select *
      into v_player
    from public.player_profiles
    where auth_user_id = p_auth_user_id
      and archived_at is null
    order by created_at asc
    limit 1
    for update;
  end if;

  if not found and v_email is not null then
    select *
      into v_player
    from public.player_profiles
    where lower(account_email) = v_email
      and archived_at is null
    order by created_at asc
    limit 1
    for update;
  end if;

  if not found and v_console is not null then
    select *
      into v_player
    from public.player_profiles
    where id_console = v_console
      and archived_at is null
    order by created_at asc
    limit 1
    for update;
  end if;

  if found and v_player.club_id is not null and v_player.club_id <> v_membership.club_id then
    raise exception using errcode = 'P0001', message = 'Il giocatore risulta gia attivo in un altro club';
  end if;

  if found then
    update public.player_profiles
    set club_id = v_membership.club_id,
        membership_id = v_membership.id,
        auth_user_id = coalesce(p_auth_user_id, auth_user_id),
        account_email = coalesce(v_email, account_email),
        nome = v_nome,
        cognome = v_cognome,
        shirt_number = coalesce(p_shirt_number, shirt_number),
        primary_role = coalesce(public.clubline_normalize_text(p_primary_role), primary_role),
        id_console = coalesce(v_console, id_console),
        team_role = v_team_role,
        archived_at = null
    where id = v_player.id
    returning * into v_player;

    return v_player.id;
  end if;

  insert into public.player_profiles (
    club_id,
    membership_id,
    nome,
    cognome,
    auth_user_id,
    account_email,
    shirt_number,
    primary_role,
    secondary_role,
    secondary_roles,
    id_console,
    team_role
  )
  values (
    v_membership.club_id,
    v_membership.id,
    v_nome,
    v_cognome,
    p_auth_user_id,
    v_email,
    p_shirt_number,
    public.clubline_normalize_text(p_primary_role),
    null,
    '{}'::text[],
    v_console,
    v_team_role
  )
  returning * into v_player;

  return v_player.id;
end;
$$;

create or replace function public.clubline_create_club(
