create or replace function public.clubline_normalize_text(value text)
returns text
language sql
immutable
as $$
  select nullif(regexp_replace(btrim(coalesce(value, '')), '\s+', ' ', 'g'), '')
$$;

create or replace function public.clubline_normalize_email(value text)
returns text
language sql
immutable
as $$
  select nullif(lower(btrim(coalesce(value, ''))), '')
$$;

create or replace function public.clubline_slugify(value text)
returns text
language sql
immutable
as $$
  select case
    when nullif(
      regexp_replace(
        regexp_replace(lower(coalesce(value, '')), '[^a-z0-9]+', '-', 'g'),
        '(^-+|-+$)',
        '',
        'g'
      ),
      ''
    ) is null then 'club'
    else regexp_replace(
      regexp_replace(lower(coalesce(value, '')), '[^a-z0-9]+', '-', 'g'),
      '(^-+|-+$)',
      '',
      'g'
    )
  end
$$;

