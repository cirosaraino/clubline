create or replace function public.clubline_sync_lineup_player_club_id()
returns trigger
language plpgsql
as $$
declare
  v_lineup_club_id bigint;
  v_player_club_id bigint;
  v_player_archived_at timestamptz;
begin
  select club_id
    into v_lineup_club_id
  from public.lineups
  where id = new.lineup_id;

  if v_lineup_club_id is null then
    raise exception using errcode = '23503', message = 'La formazione selezionata non appartiene a nessun club valido';
  end if;

  select club_id, archived_at
    into v_player_club_id, v_player_archived_at
  from public.player_profiles
  where id = new.player_id;

  if v_player_club_id is null then
    raise exception using errcode = '23503', message = 'Il giocatore selezionato non appartiene a nessun club valido';
  end if;

  if v_player_archived_at is not null then
    raise exception using errcode = 'P0001', message = 'Non puoi assegnare alla formazione un giocatore archiviato';
  end if;

  if v_player_club_id is distinct from v_lineup_club_id then
    raise exception using errcode = 'P0001', message = 'Il giocatore non appartiene al club della formazione';
  end if;

  new.club_id := v_lineup_club_id;
  return new;
end;
$$;

