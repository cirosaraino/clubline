create or replace function public.clubline_transfer_captain_role(
  p_actor_user_id uuid,
  p_target_membership_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_captain public.memberships%rowtype;
  v_target public.memberships%rowtype;
begin
  select *
    into v_target
  from public.memberships
  where id = p_target_membership_id
  for update;

  if not found then
    raise exception using errcode = '22023', message = 'Membership di destinazione non trovata';
  end if;

  select *
    into v_captain
  from public.memberships
  where auth_user_id = p_actor_user_id
    and club_id = v_target.club_id
    and status = 'active'
    and role = 'captain'
  for update;

  if not found then
    raise exception using errcode = '42501', message = 'Solo il capitano del club puo trasferire il ruolo';
  end if;
  if v_captain.id = v_target.id then
    raise exception using errcode = 'P0001', message = 'Seleziona un altro membro per il trasferimento';
  end if;
  if v_target.status <> 'active' then
    raise exception using errcode = 'P0001', message = 'Il nuovo capitano deve essere un membro attivo';
  end if;

  update public.memberships
  set role = 'player'
  where id = v_captain.id;

  update public.memberships
  set role = 'captain'
  where id = v_target.id;

  update public.player_profiles
  set team_role = 'player'
  where membership_id = v_captain.id
    and archived_at is null;

  update public.player_profiles
  set team_role = 'captain'
  where membership_id = v_target.id
    and archived_at is null;

  perform public.clubline_log_event(
    v_captain.club_id,
    v_target.id,
    p_actor_user_id,
    v_target.auth_user_id,
    'captain_transferred',
    jsonb_build_object(
      'previous_captain_membership_id', v_captain.id,
      'new_captain_membership_id', v_target.id
    )
  );

  return jsonb_build_object(
    'previous_captain_membership_id', v_captain.id,
    'new_captain_membership_id', v_target.id
  );
end;
$$;

create or replace function public.clubline_delete_club_if_valid(
  p_actor_user_id uuid
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_captain public.memberships%rowtype;
  v_active_member_count integer;
  v_club_id bigint;
begin
  select *
    into v_captain
  from public.memberships
  where auth_user_id = p_actor_user_id
    and status = 'active'
    and role = 'captain'
  for update;

  if not found then
    raise exception using errcode = '42501', message = 'Solo il capitano puo eliminare il club corrente';
  end if;

  select count(*)
    into v_active_member_count
  from public.memberships
  where club_id = v_captain.club_id
    and status = 'active';

  if v_active_member_count <> 1 then
    raise exception using errcode = 'P0001', message = 'Puoi eliminare il club solo se sei l unico membro attivo';
  end if;

  v_club_id := v_captain.club_id;

  perform public.clubline_log_event(
    v_captain.club_id,
    v_captain.id,
    p_actor_user_id,
    p_actor_user_id,
    'club_deleted',
    jsonb_build_object('club_id', v_captain.club_id)
  );

  delete from public.clubs
  where id = v_captain.club_id;

  return jsonb_build_object('club_id', v_club_id, 'deleted', true);
end;
$$;

