create or replace function public.clubline_approve_join_request(
  p_actor_user_id uuid,
  p_join_request_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_captain public.memberships%rowtype;
  v_request public.join_requests%rowtype;
  v_membership public.memberships%rowtype;
  v_player_id bigint;
begin
  select *
    into v_request
  from public.join_requests
  where id = p_join_request_id
  for update;

  if not found then
    raise exception using errcode = '22023', message = 'Richiesta di ingresso non trovata';
  end if;
  if v_request.status <> 'pending' then
    raise exception using errcode = 'P0001', message = 'Questa richiesta di ingresso e gia stata gestita';
  end if;

  select *
    into v_captain
  from public.memberships
  where auth_user_id = p_actor_user_id
    and club_id = v_request.club_id
    and status = 'active'
    and role = 'captain'
  for update;

  if not found then
    raise exception using errcode = '42501', message = 'Solo il capitano del club puo approvare la richiesta';
  end if;

  perform 1
  from public.memberships
  where auth_user_id = v_request.requester_user_id
    and status = 'active'
  for update;

  if found then
    raise exception using errcode = 'P0001', message = 'L utente appartiene gia a un club';
  end if;

  insert into public.memberships (
    club_id,
    auth_user_id,
    role,
    status
  )
  values (
    v_request.club_id,
    v_request.requester_user_id,
    'player',
    'active'
  )
  returning * into v_membership;

  v_player_id := public.clubline_attach_player_profile(
    v_membership.id,
    v_request.requester_user_id,
    v_request.requester_email,
    v_request.requested_nome,
    v_request.requested_cognome,
    null,
    v_request.requested_shirt_number,
    v_request.requested_primary_role,
    'player'
  );

  update public.join_requests
  set status = 'approved',
      decided_by_membership_id = v_captain.id,
      decided_at = timezone('utc', now())
  where id = v_request.id;

  update public.join_requests
  set status = 'expired',
      decided_by_membership_id = v_captain.id,
      decided_at = timezone('utc', now())
  where requester_user_id = v_request.requester_user_id
    and status = 'pending'
    and id <> v_request.id;

  perform public.clubline_log_event(
    v_request.club_id,
    v_membership.id,
    p_actor_user_id,
    v_request.requester_user_id,
    'join_approved',
    jsonb_build_object(
      'join_request_id', v_request.id,
      'membership_id', v_membership.id,
      'player_id', v_player_id
    )
  );

  return jsonb_build_object(
    'membership_id', v_membership.id,
    'player_id', v_player_id
  );
end;
$$;

