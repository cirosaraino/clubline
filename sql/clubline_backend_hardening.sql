      and status = 'active';

    if v_active_member_count > 1 then
      raise exception using errcode = 'P0001', message = 'Trasferisci prima il ruolo di capitano';
    end if;

    raise exception using errcode = 'P0001', message = 'Se sei l unico membro del club puoi eliminarlo direttamente';
  end if;

  perform 1
  from public.leave_requests
  where membership_id = v_membership.id
    and status = 'pending'
  for update;

  if found then
    raise exception using errcode = 'P0001', message = 'Esiste gia una richiesta di uscita pendente';
  end if;

