    club_id,
    membership_id,
    requested_by_user_id,
    status
  )
  values (
    v_membership.club_id,
    v_membership.id,
    p_actor_user_id,
    'pending'
  )
  returning * into v_request;

  perform public.clubline_log_event(
    v_membership.club_id,
    v_membership.id,
    p_actor_user_id,
    p_actor_user_id,
    'leave_requested',
    jsonb_build_object('leave_request_id', v_request.id)
  );

  return jsonb_build_object('leave_request_id', v_request.id);
end;
$$;

create or replace function public.clubline_approve_leave_request(
  p_actor_user_id uuid,
  p_leave_request_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_request public.leave_requests%rowtype;
  v_captain public.memberships%rowtype;
  v_target public.memberships%rowtype;
  v_now timestamptz := timezone('utc', now());
begin
  select *
    into v_request
  from public.leave_requests
  where id = p_leave_request_id
  for update;

  if not found then
    raise exception using errcode = '22023', message = 'Richiesta di uscita non trovata';
  end if;
  if v_request.status <> 'pending' then
    raise exception using errcode = 'P0001', message = 'Questa richiesta di uscita e gia stata gestita';
  end if;

  select *
    into v_captain
  from public.memberships
  where auth_user_id = p_actor_user_id
    and club_id = v_request.club_id
    and status = 'active'
    and role = 'captain'
  for update;

  if not found then
    raise exception using errcode = '42501', message = 'Solo il capitano del club puo approvare l uscita';
  end if;

  select *
    into v_target
  from public.memberships
  where id = v_request.membership_id
  for update;

  if not found or v_target.status <> 'active' then
    raise exception using errcode = 'P0001', message = 'La membership di uscita non e piu attiva';
  end if;
  if v_target.role = 'captain' then
    raise exception using errcode = 'P0001', message = 'Il capitano deve trasferire il ruolo prima di uscire';
  end if;

  update public.leave_requests
  set status = 'approved',
      decided_by_membership_id = v_captain.id,
      decided_at = v_now
  where id = v_request.id;

  update public.memberships
  set status = 'left',
      left_at = v_now
  where id = v_target.id;

  perform public.clubline_log_event(
    v_request.club_id,
    v_target.id,
    p_actor_user_id,
    v_target.auth_user_id,
    'leave_approved',
    jsonb_build_object('leave_request_id', v_request.id)
  );

  return jsonb_build_object('membership_id', v_target.id, 'status', 'left');
end;
$$;

